"""The controlled failure-code vocabulary.

Mirrors ``docs/failure-taxonomy.md``. A single enum is the one place these codes are defined;
the dataset validator, scorers, failure classifier, and gate all reference it, so a code can
never drift or be silently invented.
"""

from __future__ import annotations

from enum import StrEnum


class FailureCode(StrEnum):
    # --- definition ---
    CASE_SCHEMA_INVALID = "CASE_SCHEMA_INVALID"
    CASE_REFERENCE_INVALID = "CASE_REFERENCE_INVALID"
    CASE_UNAPPROVED = "CASE_UNAPPROVED"
    DATASET_HASH_MISMATCH = "DATASET_HASH_MISMATCH"
    ASSERTION_INVALID = "ASSERTION_INVALID"
    PLAN_REFERENCE_UNRESOLVED = "PLAN_REFERENCE_UNRESOLVED"
    CONFIG_INCOMPATIBLE = "CONFIG_INCOMPATIBLE"
    DUPLICATE_CASE = "DUPLICATE_CASE"
    WORKFLOW_INCOMPATIBLE = "WORKFLOW_INCOMPATIBLE"
    EMPTY_DENOMINATOR = "EMPTY_DENOMINATOR"

    # --- invocation ---
    TARGET_UNAVAILABLE = "TARGET_UNAVAILABLE"
    PROVIDER_AUTH_ERROR = "PROVIDER_AUTH_ERROR"
    PROVIDER_RATE_LIMIT = "PROVIDER_RATE_LIMIT"
    PROVIDER_TIMEOUT = "PROVIDER_TIMEOUT"
    TARGET_INTERNAL_ERROR = "TARGET_INTERNAL_ERROR"
    RETRY_EXHAUSTED = "RETRY_EXHAUSTED"

    # --- contract ---
    OUTPUT_EMPTY = "OUTPUT_EMPTY"
    OUTPUT_PARSE_ERROR = "OUTPUT_PARSE_ERROR"
    OUTPUT_SCHEMA_INVALID = "OUTPUT_SCHEMA_INVALID"
    REQUIRED_FIELD_MISSING = "REQUIRED_FIELD_MISSING"
    INVALID_ENUM = "INVALID_ENUM"
    INVALID_EVIDENCE_REFERENCE = "INVALID_EVIDENCE_REFERENCE"

    # --- business ---
    DEADLINE_MISSED = "DEADLINE_MISSED"
    DEADLINE_FALSE_POSITIVE = "DEADLINE_FALSE_POSITIVE"
    DEADLINE_FALSE_NEGATIVE = "DEADLINE_FALSE_NEGATIVE"
    RISK_UNDERCLASSIFIED = "RISK_UNDERCLASSIFIED"
    RISK_OVERCLASSIFIED = "RISK_OVERCLASSIFIED"
    MISSING_INFO_OMITTED = "MISSING_INFO_OMITTED"
    MISSING_INFO_SPURIOUS = "MISSING_INFO_SPURIOUS"
    NEEDS_ATTENTION_INCORRECT = "NEEDS_ATTENTION_INCORRECT"
    TASK_UNSUPPORTED = "TASK_UNSUPPORTED"

    # --- grounding ---
    UNSUPPORTED_MATERIAL_CLAIM = "UNSUPPORTED_MATERIAL_CLAIM"
    SOURCE_CONTRADICTION = "SOURCE_CONTRADICTION"
    EVIDENCE_NOT_FOUND = "EVIDENCE_NOT_FOUND"
    EVIDENCE_MISMATCH = "EVIDENCE_MISMATCH"
    EVIDENCE_COVERAGE_LOW = "EVIDENCE_COVERAGE_LOW"

    # --- evaluator ---
    SCORER_ERROR = "SCORER_ERROR"
    JUDGE_ERROR = "JUDGE_ERROR"
    JUDGE_OUTPUT_INVALID = "JUDGE_OUTPUT_INVALID"
    EVIDENCE_RESOLUTION_ERROR = "EVIDENCE_RESOLUTION_ERROR"
    METRIC_AGGREGATION_ERROR = "METRIC_AGGREGATION_ERROR"
    REPORT_GENERATION_ERROR = "REPORT_GENERATION_ERROR"
